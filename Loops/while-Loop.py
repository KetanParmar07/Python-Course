# Example of while loop
print('The number from 1 to 10: ')
i = 1
while i <= 10:
    print(i)
    i+=1

print('------------------------------------------')

# Example of print number 10 to 0
print('The number from 10 to 0: ')
i = 10
while i > 0:
    print(i)
    i-=1

print('------------------------------------------')

# Example of multiplication table
print('Multiplication Table of 3 is = ')
i = 1
while i <= 10:
    print('3',' x ',i,' = ',3*i)
    i+=1