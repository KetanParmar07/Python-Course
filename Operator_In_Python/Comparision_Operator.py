num1 = float(input('Enter Number1: '))
num2 = float(input('Enter Number2: '))

# here == operator check the comparision of two value is true or flase
print(num1 == num2)
# here != operator check the comparision of two value is true or flase
print(num1 != num2)
# here > operator check if num1 value is greater then num2 value or not
print(num1 > num2)
# here < operator check if num1 value is less then num2 value or not
print(num1 < num2)
# here > operator check if num1 value is greater then or equal to num2 value or not
print(num1 >= num2)
# here < operator check if num1 value is less then or equal to num2 value or not
print(num1 <= num2)