x=54
y=7

# here (and) Logical operator will check all conditions are True if yes then print statement print the True
print(x==y and x>y)
# here (or) Logical operator will check ethier one condition is True if yes then print statement print the True
print(x==y or x>y)
# here (not) Logical operator will print reverse value of given value
print(not x==y)
print(x==y and x>y or x!=y)
